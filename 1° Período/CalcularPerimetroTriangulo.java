// Calcular o perímetro de um triângulo

import java.util.Scanner;

class Perimetro{
	public static void main(String args[]){		
		
		Scanner scan = new Scanner(System.in);
		
		int lado1;
		int lado2;
		int lado3;
		int perimetro;
		
		System.out.print("Digite o valor do lado 1: ");
		lado1 = scan.nextInt();
		System.out.print("Digite o valor do lado 2: ");
		lado2 = scan.nextInt();
		System.out.print("Digite o valor do lado 3: ");
		lado3 = scan.nextInt();
		
		perimetro = lado1 + lado2 + lado3;
		
		System.out.printf("O valor do perimetro e: %d", perimetro);
	}
}
