// Transformar Velocidade de m\s para Km\h

import java.util.Scanner;

class Calcular_Velocidade{
	public static void main(String args[]){		
		
		Scanner scan = new Scanner(System.in);
		
		double velocidade1;
		double velocidade2 = 0;

		System.out.print("Digite a velocidade em m/s: ");
		velocidade1 = scan.nextDouble();
		
		velocidade2 = velocidade1 * 3.6;
		
		System.out.printf("A velocidade em km/h e: %.2f Km/h", velocidade2);
	}
}
