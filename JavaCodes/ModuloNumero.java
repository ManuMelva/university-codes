// Imprimir o módulo do número

import java.util.Scanner;

class Modulo{
	public static void main(String args[]){		
		
		Scanner scan = new Scanner(System.in);
		
		int num;
		int modulo;

		System.out.print("Digite o numero: ");
		num = scan.nextInt();
		
		if (num >= 0){
			System.out.printf("o modulo do seu numero e: %d", num);
		}
		else{
			modulo = num * (-1);
			System.out.printf("o modulo do seu numero e: %d", modulo);
		}
	}
}
